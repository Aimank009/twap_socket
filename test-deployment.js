const priceWs = new WebSocket(`${RAILWAY_URL}/ws/prices`);
let priceUpdateCount = 0;

priceWs.on('open', () => {
    console.log('‚úÖ Connected to price feed');
});

priceWs.on('message', (data) => {
    const update = JSON.parse(data.toString());
    
    if (update.type === 'connected') {
        console.log(`üì° ${update.message}`);
    } else if (update.price_usd) {
        priceUpdateCount++;
        console.log(`üíπ Price Update #${priceUpdateCount}: $${update.price_usd.toFixed(6)} (Raw: ${update.price_raw})`);
        
        // Stop after 5 price updates
        if (priceUpdateCount >= 5) {
            console.log('‚úÖ Price feed test PASSED (received 5 updates)');
            priceWs.close();
            testSettlements();
        }
    }
});

priceWs.on('error', (error) => {
    console.log('‚ùå Price feed error:', error.message);
    process.exit(1);
});

priceWs.on('close', () => {
    console.log('üì° Price feed connection closed');
});

